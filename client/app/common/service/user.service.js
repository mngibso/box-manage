'use strict';

angular.module('manageBox.common.service.user',[
  'ngResource'
])
  .factory('manageBox.common.service.UserService', function ($resource) {
    return $resource('/api/users/:id/:controller', {
      id: '@_id'
    },
    {
      changePassword: {
        method: 'PUT',
        params: {
          controller:'password'
        }
      },
      get: {
        method: 'GET',
        params: {
          id:'me'
        }
      }
	  });
  });
